//Função pergunta 1
const resultado = document.getElementById("resultado")
function verificarResposta__1(resposta) {
    if (resposta === "b"){
        resultado.innerHTML = "Resposta correta";
        resultado.style.color = "green"
    } 
    else{
        resultado.innerHTML = "Resposta correta";
        resultado.style.color = "red"
    }
}


